package com.hsbc.basiclogic;

//WAP to check a no whether it is positive negetive or zero
public class CheckPositiveOrNegetiveUsingIfelse {

	public static void main(String[] args) {
		int num=5;
		if(num==0)
			System.out.println(num+" is zero");
		else if(num>0)
			System.out.println(num+" is positive");
		else
			System.out.println(num+" is negetive");
	

	}

}
