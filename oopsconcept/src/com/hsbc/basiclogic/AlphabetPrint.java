package com.hsbc.basiclogic;

public class AlphabetPrint {
	public static void main(String[] args) {

		/* Lower Case Alphabet"*/
		System.out.println("//Lower Case Alphabet");
		for (char ch = 'a'; ch < 'z'; ch++) {
			System.out.print(ch);
		}
		
		System.out.println();
		System.out.println();
		
		/*Upper Case Alphabet"*/
		System.out.println("//Upper Case Alphabet");
		for (char ch = 'A'; ch < 'Z'; ch++) {
			System.out.print(ch);
		}
	}
}
